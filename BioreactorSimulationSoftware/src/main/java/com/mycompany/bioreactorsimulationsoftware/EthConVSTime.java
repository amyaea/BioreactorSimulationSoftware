    //This method calculates P at each time starting from 0 until the fermentation time
    public void createPlot(double Yxs, double beta, double time, double Xmax, double Xo, double U) {
        
        System.out.print("\n");
        
        double timeSum=0, XTime, ethPro;

        
        // Create a series of data points (x, y)
        XYSeries series = new XYSeries("");
        
        while(timeSum<=time){
            
            XTime = Xmax/(1+((Xmax-Xo)/Xo)*Math.exp(-U*timeSum));
            
            ethPro = Yxs * XTime + beta * timeSum;
            
            series.add(timeSum, ethPro);
            
            timeSum+=0.1;
            
            //Debug
            System.out.println("P("+timeSum+")="+ethPro);
            
        }
        

        // Create a dataset using the series
        XYSeriesCollection dataset = new XYSeriesCollection(series);

        // Create the chart
        JFreeChart chart = ChartFactory.createXYLineChart(
                "Ethanol production versus Fermentation Time",  // Chart title
                "Time (h)",            // X Axis label
                "Ethanol Production (g/L)",            // Y Axis label
                dataset,             // Dataset
                org.jfree.chart.plot.PlotOrientation.VERTICAL, // Plot orientation
                true,                // Show legend
                true,                // Use tooltips
                false                // Configure chart to generate URLs
        );

        // Create a ChartPanel (which is a Swing component to hold the chart)
        ChartPanel chartPanel = new ChartPanel(chart);
        chartPanel.setPreferredSize(new java.awt.Dimension(600, 400));  // Set size of the chart panel

        // Add the ChartPanel to the content pane of the JFrame
        getContentPane().add(chartPanel);
        
        //
        
    }
    

